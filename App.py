# GAUGE CHART
# ─────────────────────────────────────────────────────────────────────────────
def make_gauge(premium, low=15000, high=40000):
    fig, ax = plt.subplots(figsize=(5, 2.8), subplot_kw=dict(aspect="equal"),
                           facecolor="#0d0d1a")
    ax.set_facecolor("#0d0d1a")
    
    pct = min(max((premium - low) / (high - low), 0), 1)
    theta_start, theta_end = np.pi, 0
    theta = theta_start - pct * np.pi
    
    # Background arc
    angles = np.linspace(np.pi, 0, 200)
    ax.plot(np.cos(angles) * 0.8, np.sin(angles) * 0.8,
            color="#2a2a4a", linewidth=20, solid_capstyle="round", zorder=1)
    
    # Filled arc
    color_map = plt.cm.RdYlGn_r
    seg_color = color_map(pct)
    filled = np.linspace(np.pi, theta, max(int(pct * 200), 2))
    ax.plot(np.cos(filled) * 0.8, np.sin(filled) * 0.8,
            color=seg_color, linewidth=20, solid_capstyle="round", zorder=2)
    
    # Needle
    ax.annotate("", xy=(np.cos(theta)*0.65, np.sin(theta)*0.65),
                xytext=(0, 0),
                arrowprops=dict(arrowstyle="->", color="#ffd93d",
                                lw=3, mutation_scale=15))
    ax.plot(0, 0, "o", color="#ffd93d", markersize=8, zorder=5)
    
    # Labels
    ax.text(-0.85, -0.18, f"₹{low//1000}K", ha="center", va="center",
            fontsize=9, color="#aaa")
    ax.text(0.85, -0.18, f"₹{high//1000}K", ha="center", va="center",
            fontsize=9, color="#aaa")
    ax.text(0, -0.22, f"₹{premium:,.0f}", ha="center", va="center",
            fontsize=13, fontweight="bold", color="#ffd93d")
    
    ax.set_xlim(-1.1, 1.1); ax.set_ylim(-0.4, 1.05)
    ax.axis("off")
    return fig


