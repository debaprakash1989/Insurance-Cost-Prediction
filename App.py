# FACTOR IMPACT BAR
# ─────────────────────────────────────────────────────────────────────────────
def make_factor_chart(inputs_dict):
    base_factors = {
        "Age":                 inputs_dict["age"] / 66 * 100,
        "BMI":                 min(inputs_dict["bmi"] / 40 * 100, 100),
        "Health Score":        inputs_dict["health_score"] / 9 * 100,
        "Surgeries":           inputs_dict["surgeries"] / 3 * 100,
        "Chronic Diseases":    inputs_dict["chronic"] * 70,
        "Transplants":         inputs_dict["transplants"] * 85,
        "Blood Pressure":      inputs_dict["bp"] * 55,
        "Diabetes":            inputs_dict["diabetes"] * 50,
        "Cancer (Family)":     inputs_dict["cancer"] * 40,
        "Allergies":           inputs_dict["allergies"] * 25,
    }
    
    fig, ax = plt.subplots(figsize=(7, 4.5), facecolor="#1a1a2e")
    ax.set_facecolor("#1a1a2e")
    
    factors = dict(sorted(base_factors.items(), key=lambda x: x[1], reverse=True))
    colors  = ["#ff6b9d" if v > 60 else ("#ffd93d" if v > 30 else "#6bcb77")
               for v in factors.values()]
    
    bars = ax.barh(list(factors.keys()), list(factors.values()),
                   color=colors, edgecolor="#1a1a2e", height=0.65)
    
    for bar, val in zip(bars, factors.values()):
        ax.text(bar.get_width() + 1, bar.get_y() + bar.get_height()/2,
                f"{val:.0f}", va="center", ha="left", fontsize=9, color="#e0e0e0")
    
    ax.set_xlim(0, 115)
    ax.set_xlabel("Risk Contribution Score", color="#aaa", fontsize=10)
    ax.tick_params(colors="#ccc", labelsize=9)
    ax.set_title("Risk Factor Breakdown", color="#00d4ff", fontsize=12, fontweight="bold")
    
    legend_items = [
        mpatches.Patch(color="#ff6b9d", label="High impact"),
        mpatches.Patch(color="#ffd93d", label="Moderate impact"),
        mpatches.Patch(color="#6bcb77", label="Low impact"),
    ]
    ax.legend(handles=legend_items, fontsize=8, framealpha=0.3,
              loc="lower right")
    
    for spine in ax.spines.values():
        spine.set_edgecolor("#2a2a4a")
    
    plt.tight_layout()
    return fig


