# FEATURE ENGINEERING (mirrors block3)
# ─────────────────────────────────────────────────────────────────────────────
def build_features(age, diabetes, bp, transplants, chronic, height, weight,
                   allergies, cancer_history, surgeries):
    bmi = weight / ((height / 100) ** 2)
    age_bmi = age * bmi
    health_score = (diabetes + bp + transplants + chronic +
                    allergies + cancer_history + surgeries)
    
    age_group_num = pd.cut([age], bins=[17,30,40,50,60,70], labels=[1,2,3,4,5])[0]
    bmi_cat_num   = pd.cut([bmi], bins=[0,18.5,25,30,100], labels=[1,2,3,4])[0]
    
    row = {
        "Age":                       age,
        "Diabetes":                  diabetes,
        "BloodPressureProblems":     bp,
        "AnyTransplants":            transplants,
        "AnyChronicDiseases":        chronic,
        "Height":                    height,
        "Weight":                    weight,
        "KnownAllergies":            allergies,
        "HistoryOfCancerInFamily":   cancer_history,
        "NumberOfMajorSurgeries":    surgeries,
        "BMI":                       bmi,
        "Age_BMI":                   age_bmi,
        "HealthScore":               health_score,
        "AgeGroup_num":              float(age_group_num),
        "BMICategory_num":           float(bmi_cat_num),
    }
    return pd.DataFrame([row]), bmi, health_score


def classify_risk(health_score, bmi, age):
    score = 0
    if health_score >= 4:   score += 3
    elif health_score >= 2: score += 1
    if bmi >= 30:           score += 2
    elif bmi >= 25:         score += 1
    if age >= 50:           score += 2
    elif age >= 40:         score += 1
    if score >= 5:   return "High",   "risk-high"
    elif score >= 2: return "Medium", "risk-medium"
    else:            return "Low",    "risk-low"


